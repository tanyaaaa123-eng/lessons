#
# Объем всего сообщения (памяти) (I) вычисляется по формуле:
# I=L⋅i
# Здесь:L— длина сообщения (количество символов)
# i— вес одного символа сообщения
#
# Для нахождения веса одного символа (i) можно воспользоваться такой формулой:
# i=log2N
# Здесь:
# N — мощность алфавита, то есть количество уникальных символов (например, 10 цифр, 26 букв латинского алфавита и т.д.)
