# При регистрации в компьютерной системе каждому пользователю выдаётся пароль, состоящий из 7 символов
# и содержащий только символы 26-символьного набора строчных латинских букв и десяти арабских цифр.
# В базе данных для хранения сведений о каждом пользователе отведено одинаковое и минимально возможное
# целое число байт. При этом используют посимвольное кодирование паролей, все символы кодируют одинаковым
# и минимально возможным количеством бит. Кроме собственно пароля, для каждого пользователя в системе хранятся
# дополнительные сведения, для чего отведено 9 байт на одного пользователя.
